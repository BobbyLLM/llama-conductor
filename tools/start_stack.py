def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def log(event: str, status: str, **fields: Any) -> None:
    payload = {"t": _now_iso(), "event": event, "status": status}
    payload.update(fields)
    try:
        print(json.dumps(payload, ensure_ascii=False), flush=True)
    except Exception:
        print(
            f'{{"t":"{_now_iso()}","event":"{event}","status":"{status}"}}',
            flush=True,
        )


def _strip_comment(line: str) -> str:
    in_single = False
    in_double = False
    out: List[str] = []
    i = 0
    while i < len(line):
        ch = line[i]
        if ch == "'" and not in_double:
            in_single = not in_single
            out.append(ch)
        elif ch == '"' and not in_single:
            in_double = not in_double
            out.append(ch)
        elif ch == "#" and not in_single and not in_double:
            break
        else:
            out.append(ch)
        i += 1
    return "".join(out).rstrip()


def _parse_scalar(raw: str) -> Any:
    s = str(raw or "").strip()
    if s == "":
        return ""
    low = s.lower()
    if low in {"true", "yes", "on"}:
        return True
    if low in {"false", "no", "off"}:
        return False
    if low in {"null", "none", "~"}:
        return None
    if (s.startswith('"') and s.endswith('"')) or (s.startswith("'") and s.endswith("'")):
        return s[1:-1]
    try:
        if any(ch in s for ch in (".", "e", "E")):
            return float(s)
        return int(s)
    except Exception:
        return s


def _load_simple_yaml(path: Path) -> Dict[str, Any]:
    if not path.exists():
        raise FileNotFoundError(str(path))
    raw_lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
    root: Dict[str, Any] = {}
    stack: List[Tuple[int, Any]] = [(-1, root)]

    i = 0
    while i < len(raw_lines):
        original = raw_lines[i]
        line = _strip_comment(original)
        if not line.strip():
            i += 1
            continue

        indent = len(line) - len(line.lstrip(" "))
        content = line[indent:]
        while stack and indent <= stack[-1][0]:
            stack.pop()
        if not stack:
            stack = [(-1, root)]
        parent = stack[-1][1]

        if content.startswith("- "):
            item = _parse_scalar(content[2:].strip())
            if isinstance(parent, list):
                parent.append(item)
            i += 1
            continue

        if ":" not in content:
            i += 1
            continue

        key, rest = content.split(":", 1)
        key = key.strip()
        if i == 0:
            key = key.lstrip("\ufeff")
        rest = rest.strip()

        if isinstance(parent, list):
            i += 1
            continue

        if rest != "":
            parent[key] = _parse_scalar(rest)
            i += 1
            continue

        child: Any = {}
        j = i + 1
        while j < len(raw_lines):
            nline = _strip_comment(raw_lines[j])
            if not nline.strip():
                j += 1
                continue
            nindent = len(nline) - len(nline.lstrip(" "))
            ncontent = nline[nindent:]
            if nindent <= indent:
                child = {}
                break
            child = [] if ncontent.startswith("- ") else {}
            break

        parent[key] = child
        stack.append((indent, child))
        i += 1

    return root


def _yaml_quote(value: str) -> str:
    s = str(value)
    s = s.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{s}"'


def _dump_stack_local_yaml(data: Dict[str, Any]) -> str:
    lines: List[str] = [
        "## Generated by: python tools/start_stack.py --setup",
        "## This file is read by tools/start_stack.py at launch time.",
        "## Keep values blank to inherit from llama_conductor/router_config.yaml.",
    ]

    section_comments: Dict[str, List[str]] = {
        "version": [
            "## Schema version for this local file.",
        ],
        "paths": [
            "## Optional local path overrides. Empty means inherit from router_config.yaml.",
        ],
        "qdrant": [
            "## Optional Qdrant launcher settings. Set enabled: true to auto-launch Qdrant.",
            "## launch_mode: 'direct' (default) or 'cmd_start_min' (Windows parity with old batch behavior).",
        ],
        "metrics": [
            "## Optional supervisor metrics endpoint (Prometheus text format). Default is off.",
        ],
        "notes": [
            "## Auto-populated informational snapshot from router_config.yaml at setup time.",
            "## notes.* is informational only. You may edit paths/qdrant/metrics manually.",
            "## Rerun --setup only if you want notes.* refreshed.",
        ],
    }

    def emit(k: str, v: Any, ind: int) -> None:
        pad = " " * ind
        if isinstance(v, dict):
            lines.append(f"{pad}{k}:")
            for ck, cv in v.items():
                emit(str(ck), cv, ind + 2)
            return
        if isinstance(v, bool):
            lines.append(f"{pad}{k}: {'true' if v else 'false'}")
            return
        if v is None:
            lines.append(f"{pad}{k}: null")
            return
        if isinstance(v, (int, float)):
            lines.append(f"{pad}{k}: {v}")
            return
        lines.append(f"{pad}{k}: {_yaml_quote(str(v))}")

    for idx, (key, value) in enumerate(data.items()):
        if idx > 0:
            lines.append("")
        if str(key) in section_comments:
            for c in section_comments[str(key)]:
                lines.append(c)
        emit(str(key), value, 0)
    return "\n".join(lines).rstrip() + "\n"

def _get_path(mapping: Mapping[str, Any], dotted: str, default: Any = None) -> Any:
    cur: Any = mapping
    for part in dotted.split("."):
        if not isinstance(cur, Mapping) or part not in cur:
            return default
        cur = cur[part]
    return cur


def _as_bool(v: Any, default: bool = False) -> bool:
    if isinstance(v, bool):
        return v
    if v is None:
        return default
    return str(v).strip().lower() in {"1", "true", "yes", "on"}


def _as_int(v: Any, default: int) -> int:
    try:
        return int(v)
    except Exception:
        return int(default)


def _as_float(v: Any, default: float) -> float:
    try:
        return float(v)
    except Exception:
        return float(default)


