def _run_launch(res: Resolved) -> int:
    state = _MetricsState()
    metrics_server: Optional[HTTPServer] = None
    if res.metrics_enabled:
        try:
            metrics_server = _start_metrics_server(res.metrics_host, res.metrics_port, state)
            log("metrics", "ok", host=res.metrics_host, port=res.metrics_port)
        except Exception as e:
            log("metrics", "fail", error=str(e), host=res.metrics_host, port=res.metrics_port)

    if res.auto_clean:
        ports: List[Tuple[str, int]] = []
        if res.provider == "llama_cpp":
            ports.append(("upstream", res.llama_port))
        ports.append(("router", res.router_port))
        if res.shim_enabled:
            ports.append(("shim", res.shim_port))
        if res.qdrant_enabled:
            ports.append(("qdrant", res.qdrant_port))
        for label, port in ports:
            if not _preclean_port(label, port):
                return EXIT_START_FAIL

    py = sys.executable
    env = dict(os.environ)

    # Qdrant optional.
    if res.qdrant_enabled:
        if not res.qdrant_bin:
            log("qdrant", "warn", message="Qdrant not configured — ##mentats/Vault paths will be unavailable.")
            state.set("qdrant", False)
        elif not Path(res.qdrant_bin).exists():
            log("qdrant", "warn", message="Qdrant binary not found — ##mentats/Vault paths will be unavailable.", path=res.qdrant_bin)
            state.set("qdrant", False)
        else:
            qmode = str(res.qdrant_launch_mode or "direct").strip().lower()
            if os.name == "nt" and qmode == "cmd_start_min":
                qexe_full = str(Path(res.qdrant_bin))
                qproc = _spawn(
                    ["cmd.exe", "/c", f'start "" /min "{qexe_full}"'],
                    cwd=Path(res.qdrant_bin).parent,
                    env=env,
                    label="qdrant",
                )
                log("qdrant", "info", launch_mode="cmd_start_min", cwd=str(Path(res.qdrant_bin).parent), exe=qexe_full)
            else:
                qproc = _spawn([res.qdrant_bin], cwd=Path(res.qdrant_bin).parent, env=env, label="qdrant")
                log("qdrant", "info", launch_mode="direct", path=res.qdrant_bin)
            if qproc is None:
                return EXIT_START_FAIL
            qok = _wait_qdrant_http(res.qdrant_health_url, timeout_s=max(5.0, res.ready_timeout_s * 0.8))
            state.set("qdrant", qok)
            if not qok:
                log("qdrant", "warn", message="Qdrant launched but health check timed out; continuing.")

    if res.provider == "llama_cpp":
        if not res.llama_exe:
            log("config", "fail", reason="llama_cpp requires backend.llama_cpp.exe_path or stack.local override")
            return EXIT_CONFIG
        if not Path(res.llama_exe).exists():
            log("config", "fail", reason="llama_cpp exe path not found", path=res.llama_exe)
            return EXIT_CONFIG
        if not res.models_dir:
            log("config", "fail", reason="llama_cpp requires backend.llama_cpp.models_dir or stack.local override")
            return EXIT_CONFIG
        if not Path(res.models_dir).exists():
            log("config", "fail", reason="models_dir path not found", path=res.models_dir)
            return EXIT_CONFIG
        if not res.models_preset_path:
            log("config", "fail", reason="models_preset_path unresolved")
            return EXIT_CONFIG
        if not _generate_models_preset(py, res.repo_root, res.router_config, Path(res.models_preset_path), res.llama_ctx):
            return EXIT_START_FAIL

        llama_cmd = [
            res.llama_exe,
            "--models-dir", res.models_dir,
            "--models-preset", res.models_preset_path,
            "-t", str(res.llama_threads),
            "--threads-batch", str(res.llama_threads_batch),
            "-c", str(res.llama_ctx),
            "--keep", str(res.llama_keep),
            "--context-shift",
            "-b", str(res.llama_batch),
            "-ub", str(res.llama_ubatch),
            "--metrics",
            "--parallel", str(res.llama_parallel),
            "--port", str(res.llama_port),
            "--host", res.llama_host,
        ]
        if _spawn(llama_cmd, cwd=Path(res.llama_exe).parent, env=env, label="upstream") is None:
            return EXIT_START_FAIL
        up_ok = _wait_http(f"http://127.0.0.1:{res.llama_port}/v1/models", "upstream", timeout_s=res.ready_timeout_s)
        state.set("upstream", up_ok)
        if not up_ok:
            return EXIT_HEALTH_TIMEOUT

    router_cli = res.repo_root / "llama_conductor_cli.py"
    if not router_cli.exists():
        log("config", "fail", reason="missing llama_conductor_cli.py", path=str(router_cli))
        return EXIT_CONFIG
    if _spawn(
        [py, str(router_cli), "serve", "--host", res.router_host, "--port", str(res.router_port)],
        cwd=res.repo_root,
        env=env,
        label="router",
    ) is None:
        return EXIT_START_FAIL
    router_ok = _wait_http(f"http://127.0.0.1:{res.router_port}/v1/models", "router", timeout_s=res.ready_timeout_s)
    state.set("router", router_ok)
    if not router_ok:
        return EXIT_HEALTH_TIMEOUT

    if res.shim_enabled:
        shim_py = res.repo_root / "llama_conductor" / "shim" / "shim_server.py"
        if not shim_py.exists():
            log("config", "fail", reason="shim enabled but shim_server.py missing", path=str(shim_py))
            return EXIT_CONFIG
        shim_env = dict(env)
        shim_env["MOA_CHAT_HOST"] = res.shim_host
        shim_env["MOA_CHAT_PORT"] = str(res.shim_port)
        shim_env["MOA_CHAT_LLAMASERVER_URL"] = f"http://127.0.0.1:{res.llama_port}"
        shim_env["MOA_CHAT_ROUTER_URL"] = f"http://127.0.0.1:{res.router_port}"
        shim_env["MOA_CHAT_INJECT_UI"] = "1" if res.shim_inject_ui else "0"
        shim_env["MOA_CHAT_AUTH_USER"] = res.shim_auth_user
        shim_env["MOA_CHAT_PASSWORD"] = res.shim_auth_password
        shim_env["MOA_CHAT_FORCE_VISION"] = "1" if res.shim_force_vision else "0"
        shim_env["MOA_CHAT_RENDER_MODE"] = res.shim_render_mode
        shim_env["MOA_CHAT_UI_MODEL_ALIAS"] = res.shim_ui_model_alias
        shim_env["MOA_CHAT_ROUTER_MODEL_ID"] = res.shim_router_model_id
        if _spawn(
            [py, str(shim_py)],
            cwd=shim_py.parent,
            env=shim_env,
            label="shim",
        ) is None:
            return EXIT_START_FAIL
        shim_ok = _wait_http(f"http://127.0.0.1:{res.shim_port}/shim/healthz", "shim", timeout_s=res.ready_timeout_s, must_contain='"ok":true')
        state.set("shim", shim_ok)
        if not shim_ok:
            return EXIT_HEALTH_TIMEOUT

    log(
        "launch",
        "ok",
        provider=res.provider,
        upstream=(f"http://127.0.0.1:{res.llama_port}" if res.provider == "llama_cpp" else "external"),
        router=f"http://127.0.0.1:{res.router_port}",
        shim=(f"http://127.0.0.1:{res.shim_port}" if res.shim_enabled else "disabled"),
    )

    if metrics_server is not None:
        log("metrics", "info", message="metrics enabled; supervisor staying alive", endpoint=f"http://{res.metrics_host}:{res.metrics_port}/metrics")
        try:
            while True:
                time.sleep(1.0)
        except KeyboardInterrupt:
            log("metrics", "info", message="shutdown requested")
            try:
                metrics_server.shutdown()
            except Exception:
                pass

    return EXIT_OK


def _build_parser() -> argparse.ArgumentParser:
    repo_root = Path(__file__).resolve().parents[1]
    p = argparse.ArgumentParser(description="stdlib-only launcher for llama-conductor stack")
    p.add_argument("--setup", action="store_true", help="interactive first-run setup; writes stack.local.yaml")
    p.add_argument("--doctor", action="store_true", help="print resolved launch configuration (JSONL)")
    p.add_argument("--config", default=str(repo_root / "llama_conductor" / "router_config.yaml"), help="router config path")
    p.add_argument("--local-config", default=str(repo_root / "stack.local.yaml"), help="local launcher config path")
    return p


def main(argv: Optional[Iterable[str]] = None) -> int:
    args = _build_parser().parse_args(list(argv) if argv is not None else None)
    try:
        repo_root = Path(__file__).resolve().parents[1]
        router_cfg_path = Path(args.config).resolve()
        local_cfg_path = Path(args.local_config).resolve()

        if not router_cfg_path.exists():
            log("config", "fail", reason="router_config.yaml not found", path=str(router_cfg_path))
            return EXIT_CONFIG
        router_cfg = _load_simple_yaml(router_cfg_path)

        if args.setup:
            return _run_setup(router_cfg, local_cfg_path)

        local_cfg, local_err = _ensure_local_config(local_cfg_path)
        if local_err:
            if str(local_err).startswith("missing "):
                log("setup", "info", message="local config missing; auto-running setup with defaults", path=str(local_cfg_path))
                rc = _run_setup(router_cfg, local_cfg_path, non_interactive=True)
                if rc != EXIT_OK:
                    return EXIT_CONFIG
                local_cfg, local_err = _ensure_local_config(local_cfg_path)
            if local_err:
                log("config", "fail", reason=local_err)
                return EXIT_CONFIG
        assert local_cfg is not None
        resolved = _resolve_settings(repo_root, router_cfg, local_cfg)

        if args.doctor:
            return _run_doctor(resolved)
        return _run_launch(resolved)
    except KeyboardInterrupt:
        log("main", "warn", message="interrupted")
        return EXIT_UNEXPECTED
    except Exception as e:
        log("main", "fail", error=str(e))
        return EXIT_UNEXPECTED


if __name__ == "__main__":
    raise SystemExit(main())
