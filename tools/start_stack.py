def _detect_qdrant_binary() -> str:
    if os.name == "nt":
        try:
            proc = subprocess.run(["where", "qdrant.exe"], capture_output=True, text=True, check=False)
            if proc.returncode == 0:
                line = (proc.stdout or "").splitlines()[0].strip()
                if line:
                    return line
        except Exception:
            return ""
    else:
        p = shutil.which("qdrant")
        return p or ""
    return ""


def _prompt(text: str, default: str = "") -> str:
    suffix = f" [{default}]" if default else ""
    try:
        val = input(f"{text}{suffix}: ").strip()
    except EOFError:
        return default
    return val if val else default


def _run_setup(router_cfg: Dict[str, Any], local_cfg_path: Path, non_interactive: bool = False) -> int:
    log("setup", "start", local_config=str(local_cfg_path))
    models_dir_cfg = str(_get_path(router_cfg, "backend.llama_cpp.models_dir", "") or "")
    llama_exe_cfg = str(_get_path(router_cfg, "backend.llama_cpp.exe_path", "") or "")
    qdrant_guess = _detect_qdrant_binary()

    if non_interactive:
        models_dir_override = ""
        llama_exe_override = ""
        qdrant_enabled = False
        qdrant_bin = ""
    else:
        print("\n[setup] First-run local launcher config\n")
        print("Leave blank to inherit from router_config.yaml where applicable.\n")
        models_dir_override = _prompt("Models dir override (optional)", "")
        llama_exe_override = _prompt("llama-server exe override (optional)", "")
        qdrant_enable_raw = _prompt("Enable Qdrant launch? (y/N)", "N").strip().lower()
        qdrant_enabled = qdrant_enable_raw in {"y", "yes", "1", "true", "on"}
        qdrant_bin = ""
        if qdrant_enabled:
            qdrant_bin = _prompt("Qdrant binary path", qdrant_guess)

    qport = _as_int(_get_path(router_cfg, "rag.qdrant_port", 6333), 6333)
    data: Dict[str, Any] = {
        "version": 1,
        "paths": {
            "models_dir_override": models_dir_override,
            "llama_exe_override": llama_exe_override,
            "qdrant_bin": qdrant_bin,
        },
        "qdrant": {
            "enabled": bool(qdrant_enabled),
            "host": "127.0.0.1",
            "port": qport,
            "health_url": f"http://127.0.0.1:{qport}/readyz",
            "launch_mode": "direct",
        },
        "metrics": {
            "enabled": False,
            "host": "127.0.0.1",
            "port": 9919,
        },
        "notes": {
            "models_dir_inherited_from_router_config": models_dir_cfg,
            "llama_exe_inherited_from_router_config": llama_exe_cfg,
        },
    }
    local_cfg_path.write_text(_dump_stack_local_yaml(data), encoding="utf-8", newline="\n")
    log("setup", "ok", wrote=str(local_cfg_path), qdrant_enabled=qdrant_enabled, mode=("auto" if non_interactive else "interactive"))
    return EXIT_OK

def _port_open(port: int, host: str = "127.0.0.1") -> bool:
    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.settimeout(0.5)
    try:
        return sock.connect_ex((host, int(port))) == 0
    except Exception:
        return False
    finally:
        try:
            sock.close()
        except Exception:
            pass


def _find_pids_on_port(port: int) -> List[int]:
    pids: List[int] = []
    try:
        if os.name == "nt":
            proc = subprocess.run(["netstat", "-ano", "-p", "tcp"], capture_output=True, text=True, check=False)
            if proc.returncode != 0:
                return []
            needle = f":{int(port)}"
            for ln in (proc.stdout or "").splitlines():
                s = ln.strip()
                if not s or "LISTENING" not in s.upper():
                    continue
                if needle not in s:
                    continue
                parts = s.split()
                if len(parts) >= 5:
                    try:
                        pid = int(parts[-1])
                        if pid > 0:
                            pids.append(pid)
                    except Exception:
                        continue
        else:
            proc = subprocess.run(["ss", "-ltnp", f"sport = :{int(port)}"], capture_output=True, text=True, check=False)
            if proc.returncode != 0:
                return []
            for ln in (proc.stdout or "").splitlines():
                for token in ln.split():
                    if "pid=" in token:
                        start = token.find("pid=")
                        if start >= 0:
                            frag = token[start + 4 :]
                            digits = ""
                            for ch in frag:
                                if ch.isdigit():
                                    digits += ch
                                else:
                                    break
                            if digits:
                                pids.append(int(digits))
    except Exception:
        return []
    return sorted(set(pids))


def _kill_pid(pid: int) -> bool:
    try:
        if os.name == "nt":
            proc = subprocess.run(["taskkill", "/PID", str(pid), "/T", "/F"], capture_output=True, text=True, check=False)
            return proc.returncode == 0
        os.kill(pid, signal.SIGTERM)
        return True
    except Exception:
        return False


def _wait_port_free(port: int, timeout_s: float) -> bool:
    deadline = time.monotonic() + max(1.0, float(timeout_s))
    while time.monotonic() < deadline:
        if not _port_open(port):
            return True
        time.sleep(0.25)
    return not _port_open(port)


def _preclean_port(label: str, port: int) -> bool:
    pids = _find_pids_on_port(port)
    if pids:
        log("preclean", "info", label=label, port=port, pids=pids)
    for pid in pids:
        ok = _kill_pid(pid)
        log("preclean.kill", "ok" if ok else "warn", port=port, pid=pid)
    ok_free = _wait_port_free(port, timeout_s=8.0)
    log("preclean.wait_free", "ok" if ok_free else "fail", label=label, port=port)
    return ok_free


def _http_ready(url: str, timeout_s: float = 2.0, must_contain: str = "") -> bool:
    try:
        with urlopen(url, timeout=timeout_s) as resp:  # nosec B310 local probes only
            status = int(getattr(resp, "status", 0))
            if status < 200 or status >= 400:
                return False
            if must_contain:
                body = resp.read(4096).decode("utf-8", errors="ignore")
                return must_contain in body
            return True
    except URLError:
        return False
    except Exception:
        return False


def _wait_http(url: str, label: str, timeout_s: float, must_contain: str = "") -> bool:
    deadline = time.monotonic() + max(1.0, float(timeout_s))
    delay = 0.35
    while time.monotonic() < deadline:
        if _http_ready(url, timeout_s=min(2.0, delay + 0.5), must_contain=must_contain):
            log("health", "ok", label=label, url=url)
            return True
        time.sleep(delay)
        delay = min(2.0, delay * 1.4)
    log("health", "fail", label=label, url=url, timeout_s=timeout_s)
    return False


def _wait_qdrant_http(primary_url: str, timeout_s: float) -> bool:
    urls: List[str] = [str(primary_url or "").strip()]
    try:
        p = urlparse(urls[0])
        if p.scheme and p.netloc:
            root = f"{p.scheme}://{p.netloc}/"
            if root not in urls:
                urls.append(root)
    except Exception:
        pass

    deadline = time.monotonic() + max(1.0, float(timeout_s))
    delay = 0.35
    while time.monotonic() < deadline:
        for u in urls:
            if u and _http_ready(u, timeout_s=min(2.0, delay + 0.5)):
                log("health", "ok", label="qdrant", url=u)
                return True
        time.sleep(delay)
        delay = min(2.0, delay * 1.4)
    log("health", "fail", label="qdrant", url=urls[0] if urls else "", timeout_s=timeout_s)
    return False


class _MetricsState:
    def __init__(self) -> None:
        self._lock = threading.Lock()
        self.service_up: Dict[str, int] = {}

    def set(self, service: str, up: bool) -> None:
        with self._lock:
            self.service_up[service] = 1 if up else 0

    def render_prom(self) -> str:
        with self._lock:
            lines = [
                "# HELP service_up 1 if service considered healthy by supervisor",
                "# TYPE service_up gauge",
            ]
            for name in sorted(self.service_up.keys()):
                lines.append(f'service_up{{service="{name}"}} {int(self.service_up[name])}')
            return "\n".join(lines) + "\n"


class _MetricsHandler(BaseHTTPRequestHandler):
    state: _MetricsState = _MetricsState()

    def do_GET(self) -> None:  # noqa: N802
        if self.path not in {"/metrics", "/"}:
            self.send_response(404)
            self.end_headers()
            return
        payload = self.state.render_prom().encode("utf-8")
        self.send_response(200)
        self.send_header("Content-Type", "text/plain; version=0.0.4; charset=utf-8")
        self.send_header("Content-Length", str(len(payload)))
        self.end_headers()
        self.wfile.write(payload)

    def log_message(self, fmt: str, *args: Any) -> None:
        return


def _start_metrics_server(host: str, port: int, state: _MetricsState) -> HTTPServer:
    _MetricsHandler.state = state
    server = HTTPServer((host, int(port)), _MetricsHandler)
    thread = threading.Thread(target=server.serve_forever, daemon=True)
    thread.start()
    return server


def _spawn(
    cmd: List[str],
    cwd: Path,
    env: Dict[str, str],
    label: str,
) -> Optional[subprocess.Popen]:
    log("spawn", "info", label=label, cmd=cmd, cwd=str(cwd))
    kwargs: Dict[str, Any] = {"cwd": str(cwd), "env": env}
    try:
        if os.name == "nt":
            flags = 0
            flags |= getattr(subprocess, "CREATE_NEW_CONSOLE", 0)
            flags |= getattr(subprocess, "CREATE_NEW_PROCESS_GROUP", 0)
            kwargs["creationflags"] = flags
        else:
            kwargs["start_new_session"] = True
        proc = subprocess.Popen(cmd, **kwargs)  # noqa: S603
        log("spawn", "ok", label=label, pid=int(getattr(proc, "pid", 0) or 0))
        return proc
    except Exception as e:
        log("spawn", "fail", label=label, error=str(e))
        return None


def _generate_models_preset(py_exe: str, repo_root: Path, cfg_path: Path, out_path: Path, ctx_size: int) -> bool:
    script = repo_root / "llama_conductor" / "generate_models_preset.py"
    if not script.exists():
        log("preset", "fail", reason="missing generator", script=str(script))
        return False
    cmd = [py_exe, str(script), "--config", str(cfg_path), "--out", str(out_path), "--ctx-size", str(int(ctx_size))]
    try:
        proc = subprocess.run(cmd, cwd=str(repo_root), check=False)  # noqa: S603
    except Exception as e:
        log("preset", "fail", error=str(e))
        return False
    ok = proc.returncode == 0 and out_path.exists()
    log("preset", "ok" if ok else "fail", returncode=int(proc.returncode), out=str(out_path))
    return ok


def _ensure_local_config(path: Path) -> Tuple[Optional[Dict[str, Any]], Optional[str]]:
    if not path.exists():
        return None, f"missing {path}. Run: python tools/start_stack.py --setup"
    try:
        cfg = _load_simple_yaml(path)
    except Exception as e:
        return None, f"invalid {path}: {e}. Run: python tools/start_stack.py --setup"
    if _as_int(_get_path(cfg, "version", 0), 0) < 1:
        return None, f"incomplete {path} (version missing). Run: python tools/start_stack.py --setup"
    return cfg, None

